import math

# FLOOR redondear hacia abajo
resultado = math.floor(89.665)
print(resultado)
# CEIL redondear hacia arriba
resultado = math.ceil(89.665)
print(resultado)
# PI
resultado = math.pi
print(resultado)
# LOG trigonometría logaritmo
# en funcion de un número y una base por cuanto multiplicar la base para que me de el numero
resultado = math.log(25, 5)
print(resultado)
# TAN
resultado = math.tan(89)
print(resultado)
# COS
resultado = math.cos(89)
print(resultado)

# para matemáticas mas avanzadas
